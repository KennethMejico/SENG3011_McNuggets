In a little more than 15 days, the region has registered 40 dead monkeys in the cities of Santa Rosa de Lima, Rio Fortuna, Sao Martinho, Grao-Para and Braco do Norte, with 24 in just one week.

Teams from regional Health Surveillance and the Zoonoses Surveillance Unit (UVZ), Tubarao Municipal Health Foundation (FMS), issued an alert about the possibility of yellow fever cases that could be causing the deaths of monkeys in municipalities of the region.

Several days ago, 8 dead monkeys were collected that were encountered in the Santa Rosa de Lima region, the results of which still have not been issued. However, the alert was made due to the increase in cases.

According to the biologist Sabrina Fernandes Cardoso of the State Health Secretariat, the 1st results of the test are expected at the beginning of next week. \"It is worrisome because there is no apparent cause of death of these animals, with no signs of aggression or accident. In addition to this, the monkeys that were seen constantly by the residents have disappeared,\" she stated.

The alert has been issued because the appearance of dead monkeys in a given region could indicate the presence of the virus that causes yellow fever. When such an event is detected, the viscera of the animals is taken out and sent for testing in the laboratory in order to confirm, or not, the presence of this virus in these animals, which are considered to be sentinels.

\"The entire locality is being mapped for vaccination coverage of the residents. Epidemiological surveillance teams are visiting house-by-house and orienting the residents. A point to underscore is the increase in vaccination against yellow fever, which is available in all the health posts,\" stated Sabrina, the biologist.

Municipal residents who by chance encounter a dead monkey are being instructed to go to the nearest health unit to report it. Then, the health team must note the data and communicate it to the Municipal Epidemiological Surveillance, which will take the necessary measures.

