[West Nile virus (WNV) is now established in the contiguous 48 states of the USA, and cases can be expected in any of them, including Massachusetts, during the transmission season. So far this year (2020), the CDC reports 81 cases in 31 states (https://www.cdc.gov/westnile/statsmaps/preliminarymapsdata2020/disease-cases-state-2020.htm).

The 3 recent cases in Middlesex county, Massachusetts suggest that there may be a focus of transmission in that locality of the state. Transmission of WNV across the USA will occur until the onset of killing frosts ends mosquito activity for the year.

Since a commercial human WNV vaccine is not available, individuals living in localities where human or horse cases have occurred this summer (2020), or where positive mosquitoes have been trapped, should follow the recommendations given above to avoid mosquito bites. Horse owners should vaccinate their animals. - Mod.TY

