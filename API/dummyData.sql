Massachusetts health officials this week announced 4 new human cases of West Nile virus, bringing the state's total this year [2020] to 7.

Officials said that 3 males and one female were infected. A male in his 40s, one in his 60s, and one in his 80s were all exposed to the virus in Middlesex County. The 4th case was reported in a female under the age of 19. She was exposed in Bristol County, per a news release from the Massachusetts Department of Public Health. Their conditions were not made clear.

West Nile virus -- which was 1st reported in the U.S. in 1999 -- is typically spread by infected mosquitoes. Though side effects can be severe, most people who are infected experience little to no symptoms and fully recover.

A small percentage of people infected with West Nile virus -- roughly 1 in 5 -- develop a fever and may additionally experience headaches, body aches, vomiting, diarrhea, or a rash, among other side effects. Even rarer, about 1 in 150 people who are infected with the mosquito-linked ailment can develop a serious illness, such as inflammation of the spinal cord or brain, according to the Centers for Disease Control and Prevention (CDC) [https://www.cdc.gov/westnile/].

The Mayo Clinic warns that people who are older, as well as those with pre-existing medical conditions, are more susceptible to the virus.

Wearing insect repellent and protective clothing, as well as draining standing water around gardens and homes where mosquitoes can lay eggs, can be helpful in reducing the risk of sustaining a mosquito bite, ultimately mitigating the risk of developing West Nile virus.

This year [2020], the Bay State has reported 7 human cases of West Nile virus. In 2019, the state saw 5.

